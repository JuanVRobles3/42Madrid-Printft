#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*aux;
	int		i;

	aux = lst;
	i = 0;
	while (aux != NULL)
	{
		i++;
		aux = aux->next;
	}
	return (i);
}
